            Most of us have experienced anxiety, distance, isolation, empathy, and uncertainty through the lockdown. The pandemic has made us reflect and examine the way in which we perceive ourselves and our surroundings. Being confined in our homes - comfortable spaces with familiar objects - has never felt so alien.
Our workshop began with an interactive icebreaker session. We were encouraged by our mentor to create and respond to keywords related to the lockdown. Sound, Transportation, Meditation, and Touch, to name a few, which became central concepts to each individual's work. As the workshop progressed, we completed short activities related to these keywords that shaped the final projects.</p>
